const hari = 7;
let hasil;
switch (hari) {
    case 1:
        hasil = "Sunday";
        break;
    case 2:
        hasil = "Monday";
        break;
    case 3:
        hasil = "tuesdy";
        break;
    case 4:
        hasil = "wednesday";
        break;
    case 5:
        hasil = "Thursday";
        break;
    case 6:
        hasil = "Friday";
        break;
    case 7:
        hasil = "Saturday";
        break;
        

    default:
        hasil = "Hari ini tidak ada"
        break;

}
console.log(hasil);

